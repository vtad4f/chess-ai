

#include "Error.h"


////////////////////////////////////////////////////////////////////////////////
///
///   @brief  Constructor
///
///   @param errMsg  Cache this error message
///
////////////////////////////////////////////////////////////////////////////////
Error::Error(const std::string& errMsg)
   : exception()
   , m_ErrMsg(errMsg)
{
   
}


////////////////////////////////////////////////////////////////////////////////
///
///   @brief  Get the cached error message from the constructor
///
///   @return  The error message
///
////////////////////////////////////////////////////////////////////////////////
const char* Error::what() const noexcept
{
   return m_ErrMsg.c_str();
}

